package com.example.virtual_threads_demo.dto;

public class GeoDto {
    private String lat;
    private String lng;

    public GeoDto() {}

    public String getLat() {
        return lat;
    }
    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }
    public void setLng(String lng) {
        this.lng = lng;
    }
}
